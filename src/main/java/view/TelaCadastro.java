     /**
     * Método acionado ao clicar no botão de "Cadastrar".
     * Realiza validações dos campos, cria um objeto Podcast e o salva no banco.
     */
    private void btnCadastrarPodcastActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarPodcastActionPerformed
        String produtor = txtProdutor.getText();
        String nomeEpisodio = txtNomeEpisodio.getText();
        int numeroEpisodio;
        String duracao = txtDuracao.getText();
        String url = txtUrl.getText();

        if(produtor.isEmpty() || nomeEpisodio.isEmpty()|| duracao.isEmpty()||url.isEmpty()){
            JOptionPane.showMessageDialog(this, 
            "Por favor, preencha todos os campos obrigatórios!", "Aviso", 
            JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            numeroEpisodio = Integer.parseInt(txtNumeroEpisodio.getText());
            if (numeroEpisodio <= 0) {
                JOptionPane.showMessageDialog(this, 
                    "O número do episódio deve ser maior que zero!", 
                    "Aviso", 
                    JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Insira um número válido no campo 'Número do Episódio'!", 
                "Erro", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        Podcast podcast = new Podcast();
        podcast.setProdutor(produtor);
        podcast.setNomeEpisodio(nomeEpisodio);
        podcast.setNumeroEpisodio(numeroEpisodio);
        podcast.setDuracao(duracao);
        podcast.setUrl(url);

        

        PodcastDAO podcastDAO = new PodcastDAO();
        podcastDAO.salvar(podcast);
        // Limpa os campos e foca no primeiro campo após cadastro bem-sucedido
        txtProdutor.setText(""); 
        txtNomeEpisodio.setText("");
        txtNumeroEpisodio.setText("");
        txtDuracao.setText("");
        txtUrl.setText("");

        JOptionPane.showMessageDialog(this, "Podcast cadastrado com sucesso!");
    }//GEN-LAST:event_btnCadastrarPodcastActionPerformed
