    private void telas(){
        if (tipoUsuario == Usuario.TipoUsuario.Administrador) {
            SwingUtilities.invokeLater(() -> new TelaListagemPodcast().setVisible(true));
            this.dispose();
        } else{
            SwingUtilities.invokeLater(() -> new TelaListagemGeral().setVisible(true));
            this.dispose();
        }
        
    }

